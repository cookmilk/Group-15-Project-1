    public static class SettingsFragment extends PreferenceFragment implements SharedPreferences.OnSharedPreferenceChangeListener {

        private SeekBarPreference _seekBarPref;
        private SeekBarPreference _seekBarPref2;

        @Override
        public void onCreate(Bundle savedInstanceState) {

            super.onCreate(savedInstanceState);

            // Load the preferences from an XML resource
            addPreferencesFromResource(R.xml.pref_slider);


            //For steps
            // Get widgets :
            _seekBarPref = (SeekBarPreference) this.findPreference("SEEKBAR_VALUE");
            // Set listener :
            getPreferenceScreen().getSharedPreferences().registerOnSharedPreferenceChangeListener(this);
            // Set seekbar summary :
            int radius = PreferenceManager.getDefaultSharedPreferences(this.getActivity()).getInt("SEEKBAR_VALUE", 12);
            _seekBarPref.setSummary(this.getString(R.string.settings_summary).replace("$1", "" + radius));

            //For sleep
            // Get widgets :
            _seekBarPref2 = (SeekBarPreference) this.findPreference("SEEKBAR_VALUE2");
            // Set listener :
            getPreferenceScreen().getSharedPreferences().registerOnSharedPreferenceChangeListener(this);
            // Set seekbar summary :
            int radius2 = PreferenceManager.getDefaultSharedPreferences(this.getActivity()).getInt("SEEKBAR_VALUE2", 1000);
            _seekBarPref2.setSummary(this.getString(R.string.settings_summary).replace("$1", "" + radius2));
        }

        @Override
        public void onSharedPreferenceChanged(SharedPreferences sharedPreferences, String key) {

            // Set seekbar summary :
            int radius = PreferenceManager.getDefaultSharedPreferences(this.getActivity()).getInt("SEEKBAR_VALUE", 12);
            _seekBarPref.setSummary(this.getString(R.string.settings_summary).replace("$1", ""+radius));

            // Set seekbar summary :
            int radius2 = PreferenceManager.getDefaultSharedPreferences(this.getActivity()).getInt("SEEKBAR_VALUE2", 1000);
            _seekBarPref2.setSummary(this.getString(R.string.settings_summary).replace("$1", ""+radius2));
        }
    }
